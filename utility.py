def intToMonthParser(number):
  if number == 1:
    return 'Jan'
  elif number == 2:
    return 'Feb'
  elif number == 3:
    return 'Mar'
  elif number == 4:
    return 'Apr'
  elif number == 5:
    return 'May'
  elif number == 6:
    return 'Jun'
  elif number == 7:
    return 'July'
  elif number == 8:
    return 'Aug'
  elif number == 9:
    return 'Sep'
  elif number == 10:
    return 'Oct'
  elif number == 11:
    return 'Nov'
  elif number == 12:
    return 'Dec'
